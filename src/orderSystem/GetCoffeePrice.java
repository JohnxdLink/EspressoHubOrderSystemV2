package orderSystem;

public class GetCoffeePrice {
	private final double BLK_COFFEE_PRICE = 49.00;
	private final double LATTE_PRICE = 89.00;
	private final double CAPPUCCCINO_PRICE = 129.00;
	private final double AMERICANO_PRICE = 179.00;
	private final double ESPRESSO_PRICE = 219.00;
	
	
	public double getBlkCoffeePrice() {
		return BLK_COFFEE_PRICE;
	}
	
	public double getLattePrice() {
		return LATTE_PRICE;
	}
	
	public double getCappuccinoPrice() {
		return CAPPUCCCINO_PRICE;
	}
	
	public double getAmericanoPrice() {
		return AMERICANO_PRICE;
	}
	
	public double getEspressoPrice() {
		return ESPRESSO_PRICE;
	}
	
}
